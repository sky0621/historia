import fs from "node:fs";
import path from "node:path";

const rootDir = process.cwd();
const partsDir = path.join(rootDir, "src/db/schema-parts");
const outputPath = path.join(rootDir, "src/db/schema.sql");

const partFiles = [
  "000-master.sql",
  "020-event.sql",
  "010-person.sql",
  "010-person-link.sql",
  "030-period.sql",
  "030-period-link.sql",
  "040-region.sql",
  "040-region-parent-link.sql",
  "050-polity.sql",
  "050-polity-link.sql",
  "060-religion.sql",
  "060-religion-link.sql",
  "060-religion-parent-link.sql",
  "070-source.sql",
  "080-other.sql",
  "090-import.sql",
  "020-event-link.sql"
];

const header = [
  "-- This file is generated by scripts/generate-schema-sql.mjs.",
  "-- Edit files under src/db/schema-parts/ instead.",
  ""
].join("\n");

const body = partFiles
  .map((file) => fs.readFileSync(path.join(partsDir, file), "utf8").trim())
  .filter(Boolean)
  .join("\n\n");

fs.writeFileSync(outputPath, `${header}${body}\n`);
